import java.util.Scanner;
public class SumExceptSelf {
        
        public static void main(String[] args) {
                Scanner sc = new Scanner(System.in);
                int n = sc.nextInt();
                int[] arr = new int[n];
                int[] ans = new int[n];
                int totalSum = 0;
                for (int i = 0; i < n; i++) {
                        arr[i] = sc.nextInt();
                        totalSum += arr[i];
                }
                for(int i=0;i<n;i++){
                        ans[i] = totalSum - arr[i];
                }

                for(int i=0;i<n;i++){
                        System.out.print(ans[i] + " ");
                }
        }
}
